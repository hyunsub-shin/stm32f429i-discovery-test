void dump_mem(uint16_t *buf, uint16_t len)
{
    int     i,j;
    char    ch;
    uint8_t *mem_buf = buf;

    for (i=0; i<len; i+=16)
    {
	  	printf("\r\n%04X: ", i);

        for (j=0; j<16; j++)
        {
            printf("%02X", mem_buf[i+j]);
            if ((j&3) == 3)
                printf(" ");
        }
        printf("| ");
        for (j=0; j<16; j++)
        {
            ch = mem_buf[i+j];
            if((ch < 0x20) || (ch >= 0x7F))
                ch = '.';
            printf("%c", ch);
        }
    }
    printf("\r\n");
}

void buff_clr(uint16_t *buf, uint16_t len)
{
	int i;
	for(i=0;i<len;i++)
	{
		buf[i] = 0;
	}
}
